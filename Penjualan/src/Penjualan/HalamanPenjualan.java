    private void btn_prosesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_prosesActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tabledata.getModel();
        Object[] data = new Object[7];
        data[0] = combox_namabunga.getSelectedItem();
        data[1] = txt_harga.getText();
        data[2] = txt_jml.getText();
        data[3] = txt_diskon.getText();
        if(cb_kirim.isSelected()==true && rb_5km.isSelected() == true){
            data[4] = "5 KM";
        } else if(cb_kirim.isSelected() == true && rb_5to15km.isSelected() == true){
            data[4] = "5 - 15 KM";
        } else{
            data[4] = "15 KM";
        }
        //data[4] = rb_5km.getSelectedObjects();
        data[5] = txt_biayakirim.getText();
        data[6] = txt_totalbayar.getText();
        
        model.addRow(data);
        
        int totalbungaterjual = 0;
        int totalpendapatan = 0;
        for(int i = 0; i<tabledata.getRowCount(); i++){
            int jmlterjual = Integer.parseInt((String)tabledata.getValueAt(i, 2));
            int pendapatan = Integer.parseInt((String)tabledata.getValueAt(i, 6));
            totalbungaterjual += jmlterjual;
            totalpendapatan += pendapatan; 
        }
        txt_ttlterjual.setText(""+totalbungaterjual);
        txt_ttlpendapatan.setText(""+totalpendapatan);
    }//GEN-LAST:event_btn_prosesActionPerformed

