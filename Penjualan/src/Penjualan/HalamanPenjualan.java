    private void rb_5kmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rb_5kmActionPerformed
        // TODO add your handling code here:
        int biayakirim, diskon, jml, hargabunga,total, totalbayar;
        if(cb_kirim.isSelected() == true && rb_5km.isSelected() == true){
           rb_5to15km.setEnabled(false);
           rb_15km.setEnabled(false);
           txt_biayakirim.setText("0");
        } else{
           rb_5to15km.setEnabled(true);
           rb_15km.setEnabled(true);
           txt_biayakirim.setText("0");
        }
        biayakirim = Integer.parseInt(txt_biayakirim.getText());
        diskon = Integer.parseInt(txt_diskon.getText());
        jml = Integer.parseInt(txt_jml.getText());
        hargabunga = Integer.parseInt(txt_harga.getText());
        total = Integer.parseInt(txt_totalbayar.getText());
        
        totalbayar = total + biayakirim;
        txt_totalbayar.setText(""+totalbayar);
        
        
    }//GEN-LAST:event_rb_5kmActionPerformed

