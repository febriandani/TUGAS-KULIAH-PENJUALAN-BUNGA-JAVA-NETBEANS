    private void txt_jmlKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_jmlKeyReleased
        // TODO add your handling code here:
        int harga, diskon, jml, total, totalbayar, biayakirim;
        
        harga = Integer.parseInt(txt_harga.getText());
        jml = Integer.parseInt(txt_jml.getText());
       
        total = harga * jml;
        if(total >= 300000){
            diskon = (5 * total) / 100;
            txt_diskon.setText(""+diskon);
            label9.setText("");
        } else{
            txt_diskon.setText("0");
            label9.setText("Minimum pembelian diatas 300 ribu dapat diskon 5%");
        }
        diskon = Integer.parseInt(txt_diskon.getText());
        totalbayar = total - diskon;
        
        txt_totalbayar.setText(""+totalbayar);
        
        
        
    }//GEN-LAST:event_txt_jmlKeyReleased

